/**
 * Validates if the content contains a dense "AI Summary" bulleted list early in the text.
 * @param {string} content 
 * @returns {boolean} True if an AI summary exists near the start
 */
export const checkAiSummaryStructure = (content) => {
  if (!content) return false;
  // Look for a block of bullet points within the first 500 characters
  const earlyText = content.substring(0, 500);
  const bulletMatches = earlyText.match(/(?:^|\n)\s*[-*•]\s+.+/g);
  // We consider an AI Summary valid if there are at least 3 dense bullet points early on
  return bulletMatches && bulletMatches.length >= 3;
};

/**
 * Parses and strictly validates JSON-LD Schema.
 * @param {string} schemaString Raw JSON-LD string
 * @returns {object} { isValid: boolean, foundTypes: array, deprecated: array, msg: string }
 */
export const validateLdJsonSchema = (schemaString) => {
  try {
    const schemas = JSON.parse(schemaString);
    const schemaArray = Array.isArray(schemas) ? schemas : [schemas];
    
    const validTypes = ['Article', 'NewsArticle', 'FAQPage', 'Product', 'Organization'];
    const deprecatedTypes = ['HowTo', 'QAPage']; // HowTo is visually deprecated in many SERP layouts by Google
    
    let foundTypes = [];
    let deprecated = [];

    schemaArray.forEach(schema => {
      const type = schema['@type'];
      if (type) {
        foundTypes.push(type);
        if (deprecatedTypes.includes(type)) {
          deprecated.push(type);
        }
      }
    });

    const isOptimized = foundTypes.some(t => validTypes.includes(t)) && deprecated.length === 0;

    return {
      isValid: isOptimized,
      foundTypes,
      deprecated,
      msg: isOptimized ? 'Schema is highly optimized for AEO.' : 'Schema needs AEO restructuring.'
    };
  } catch (e) {
    return { isValid: false, foundTypes: [], deprecated: [], msg: 'Invalid JSON-LD format.' };
  }
};

