/**
 * Splits text into passages and analyzes them for optimal Generative Engine citability lengths.
 * Google AI Overviews and Perplexity pull quotes most frequently if they are between 134-167 words.
 * @param {string} text 
 * @returns {array} Array of passage objects: { text, wordCount, isOptimal }
 */
export const analyzePassageCitability = (text) => {
  if (!text) return [];
  
  // Split roughly by standalone paragraphs or double newlines
  const rawPassages = text.split(/\n\s*\n/).filter(p => p.trim().length > 0);
  
  return rawPassages.map(passage => {
    const wordCount = passage.trim().split(/\s+/).length;
    // According to GEO research, 134 to 167 words is the sweet spot for standalone definitive claims.
    const isOptimal = wordCount >= 134 && wordCount <= 167;
    return {
      text: passage,
      wordCount,
      isOptimal
    };
  });
};

